from collections import Counter


class Solution:
    def countCharacters(self, words, chars: str) -> int:
        c = Counter(chars)
        l = 0
        for w in words:
            cc = c.copy()
            for i, val in enumerate(w):
                if val not in cc or cc[val] == 0:
                    break
                cc[val] -= 1
                if cc[val] >= 0 and i == len(w)-1:
                    print(w, chars, c)
                    l += len(w)
        return l


